
#create variable 'types_of_people'
types_of_people = 10
#binds string with variable to x
x = "There are #{types_of_people} types of people."
#binds string to variable name
binary = "binary"
do_not = "don't"
#binds string with two variable names to y
y = "Those who know #{binary} and those who #{do_not}."

#prints values of x and y
puts x
puts y

#prints string with contents of x and y
puts "I said: #{x}."
puts "I also said: '#{y}'."

#binds boolean to variable
hilarious = false
#binds string and variable containing boolean
joke_evaluation = "Isn't that joke so funny?! #{hilarious}"

#prints output of variable
puts joke_evaluation

